package com.anahuac.rest.api.Entity.db

class InformacionEscolar {
	
	private String preparatoria;
	private String promedio;
	private String clave;
	private String ciudad;
	private String estado;
	private String pais;
	private String costoMensual;
	private String certificado;
	
	public String getPreparatoria() {
		return preparatoria;
	}
	public void setPreparatoria(String preparatoria) {
		this.preparatoria = preparatoria;
	}
	public String getPromedio() {
		return promedio;
	}
	public void setPromedio(String promedio) {
		this.promedio = promedio;
	}
	public String getClave() {
		return clave;
	}
	public void setClave(String clave) {
		this.clave = clave;
	}
	public String getCiudad() {
		return ciudad;
	}
	public void setCiudad(String ciudad) {
		this.ciudad = ciudad;
	}
	public String getEstado() {
		return estado;
	}
	public void setEstado(String estado) {
		this.estado = estado;
	}
	public String getPais() {
		return pais;
	}
	public void setPais(String pais) {
		this.pais = pais;
	}
	public String getCostoMensual() {
		return costoMensual;
	}
	public void setCostoMensual(String costoMensual) {
		this.costoMensual = costoMensual;
	}
	public String getCertificado() {
		return certificado;
	}
	public void setCertificado(String certificado) {
		this.certificado = certificado;
	}
}
