package com.anahuac.rest.api.Entity.Custom

class CatHermanoCustom {
	private Long caseid;
	private String nombre;
	private String apellidos;
	private String fechanacimiento;
	private Boolean isestudia;
	private String escuelaestudia;
	private Boolean istrabaja;
	private String empresatrabaja;
	public Long getCaseid() {
		return caseid;
	}
	public void setCaseid(Long caseid) {
		this.caseid = caseid;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public String getApellidos() {
		return apellidos;
	}
	public void setApellidos(String apellidos) {
		this.apellidos = apellidos;
	}
	public String getFechanacimiento() {
		return fechanacimiento;
	}
	public void setFechanacimiento(String fechanacimiento) {
		this.fechanacimiento = fechanacimiento;
	}
	public Boolean getIsestudia() {
		return isestudia;
	}
	public void setIsestudia(Boolean isestudia) {
		this.isestudia = isestudia;
	}
	public String getEscuelaestudia() {
		return escuelaestudia;
	}
	public void setEscuelaestudia(String escuelaestudia) {
		this.escuelaestudia = escuelaestudia;
	}
	public Boolean getIstrabaja() {
		return istrabaja;
	}
	public void setIstrabaja(Boolean istrabaja) {
		this.istrabaja = istrabaja;
	}
	public String getEmpresatrabaja() {
		return empresatrabaja;
	}
	public void setEmpresatrabaja(String empresatrabaja) {
		this.empresatrabaja = empresatrabaja;
	}
}
