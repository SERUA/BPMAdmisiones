package com.anahuac.rest.api.Entity.Custom

class CatIdiomaCustom {
	private Long caseid;
	private String otroidioma;
	private String idiomahablas;
	private String conversacion;
	private String escritura;
	private String traduccion;
	private String lectura;
	public Long getCaseid() {
		return caseid;
	}
	public void setCaseid(Long caseid) {
		this.caseid = caseid;
	}
	public String getOtroidioma() {
		return otroidioma;
	}
	public void setOtroidioma(String otroidioma) {
		this.otroidioma = otroidioma;
	}
	public String getIdiomahablas() {
		return idiomahablas;
	}
	public void setIdiomahablas(String idiomahablas) {
		this.idiomahablas = idiomahablas;
	}
	public String getConversacion() {
		return conversacion;
	}
	public void setConversacion(String conversacion) {
		this.conversacion = conversacion;
	}
	public String getEscritura() {
		return escritura;
	}
	public void setEscritura(String escritura) {
		this.escritura = escritura;
	}
	public String getTraduccion() {
		return traduccion;
	}
	public void setTraduccion(String traduccion) {
		this.traduccion = traduccion;
	}
	public String getLectura() {
		return lectura;
	}
	public void setLectura(String lectura) {
		this.lectura = lectura;
	}
}
