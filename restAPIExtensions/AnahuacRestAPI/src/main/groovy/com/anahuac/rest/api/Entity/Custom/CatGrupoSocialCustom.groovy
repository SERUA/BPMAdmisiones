package com.anahuac.rest.api.Entity.Custom

class CatGrupoSocialCustom {
	private Long caseid;
	private String nombre;
	private String afiliacion;
	private String tiempo;
	private String pertenecesaunclub;
	private String gruposocial;
	public Long getCaseid() {
		return caseid;
	}
	public void setCaseid(Long caseid) {
		this.caseid = caseid;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public String getAfiliacion() {
		return afiliacion;
	}
	public void setAfiliacion(String afiliacion) {
		this.afiliacion = afiliacion;
	}
	public String getTiempo() {
		return tiempo;
	}
	public void setTiempo(String tiempo) {
		this.tiempo = tiempo;
	}
	public String getPertenecesaunclub() {
		return pertenecesaunclub;
	}
	public void setPertenecesaunclub(String pertenecesaunclub) {
		this.pertenecesaunclub = pertenecesaunclub;
	}
	public String getGruposocial() {
		return gruposocial;
	}
	public void setGruposocial(String gruposocial) {
		this.gruposocial = gruposocial;
	}

}
